// loop ////////////////////////////////////////////////////////////////////////
// /////////////////////////////////////////////////////////////////////////////

function loop(val, test, step, body) {
  //base case
  if(!test(val)) {
    return val;
  }
  //code

  //recursion
  body(val);
  return loop(step(val), test, step, body);
}

// /////////////////////////////////////////////////////////////////////////////
// every ///////////////////////////////////////////////////////////////////////
// /////////////////////////////////////////////////////////////////////////////
//every item in array has to pass the test for the function to return true

function every(arr, test) {
  for(let i = 0; i < arr.length; i++) {
    if(!test(arr[i])) {
      return false;
    }
  }
  return true;
}
