// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
//accepts one number as argument
//returns true if number is even, false otherwise
function isEven(num) {
  //check for negative number
  if (num < 0) {
    //if num is negetive then multiply it by -1
    num *= -1;
  }
  //check if num divided by 2 is zero
  //return the boolean value
  return num % 2 === 0;
}

