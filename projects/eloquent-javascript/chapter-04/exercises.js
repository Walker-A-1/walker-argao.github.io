//reverseArray takes an array and produces a new array in the reverse order
function reverseArray(array, result=[]) {
  //base
  if(array.length === 0) {
    return result;
  }
  //recersive
  result.unshift(array[0]);
  return reverseArray(array.slice(1), result);
}

