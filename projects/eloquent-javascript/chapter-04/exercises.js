////////////////////////////////////////////////////////////////////////////////

function arrayToList(arr) {
  //initialize `rest` at `null`
  let rest = null;
  //for loop decrementing through <arr>
  for(let i = arr.length - 1; i >= 0; i--) {
    rest = { value: arr[i], rest: rest }
  }
  //return rest
  return rest;
}

