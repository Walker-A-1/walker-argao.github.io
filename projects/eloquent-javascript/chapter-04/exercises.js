// sum /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function sum(arr) {
  //initialize res at 0
  let res = 0;
  //for loop for <arr>
  for(let i = 0; i < arr.length; i++) {
    //add current <arr> element to res
    res += arr[i];
  }
  //return res
  return res;
}

